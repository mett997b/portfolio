                <h3>Kanyleteorien</h3>
                <p>
                    Der findes to ”kanyle-modeller”, men begge med den samme teori. Kanyleteorien går ud på at man tror på, at man metaforisk sagde, kan sprøjte ideer og holdninger ind i modtageren, ved hjælp af propaganda. Ideen kommer i, at man på de sociale medier bliver påvirket. 
                    <br>Ser man fx for mange billeder af solbrune kvinder, uden fedt på kroppen, bliver man påvirket til at tro, at det er standart idealet for kvinder.
                </p>

                    <h4>S-R-modellen</h4>
                    <p>
                        ”S” betyder stimulus og ”R” betyder respons og bygger på antagelsen af, at menneske kan blive styret af stimulering. 
                        <br>Hver gang afsender, sender noget ud kommer der en respons fra modtageren. S-R-modellen bliver brugt meget i reklamer.
                        <br>Jo flere gentagelser af den samme reklamer og kraftigere budskab, desto større respons.
                    </p>
