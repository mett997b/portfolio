        <h1>Kommunikationsteori</h1>
        <p>
            Når det kommer til at vælge en kommunikationsløsning, er det vigtigt, at der kan dokumenteres for hvorfor lige netop det specifikke løsning er den rigtige. 
            <br>Det er derfor vigtigt at have styr på de forskellige kommunikationsmodeler, og hvilke antagelser der høre til dem, hver især.
        </p>
