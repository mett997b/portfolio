                <h3>IMK-modellen</h3>
                <p>
                    I IMK-modellen er det teksten der er i fokus, afsender og modtager er derfor ligeværdige i produktionen. 
                </p>
                <p>
                    Modellen er opbygget af kontekst/kultur, Medier og genre til at danne ramme om teksten. 
                    <br>Det er med denne model, vigtigt at der er god kommunikation mellem afsender og modtager, og det derfor ikke er nok at der bliver sendt et budskad ud, uden deltagelse fra modtageren.
                </p>
                <img src="imk.JPG" alt="IMK-modellen">
                <p>
                    -	Afsenderen er den rolle, der bestemmer hvad og hvornår kommunikationen finder sted.
                    <br>-	Modtageren er den antagende men også forstående rolle, der skal fortolke kommunikationen.
                    <br>-	Teksten er den primære kommunikation mellem afsender og modtager.
                    <br>-	Referenten er alt hvad teksten referer til.
                    <br>-	Koden er det sprog modtageren skal kunne for at skabe forståelse.
                    <br>-	Kontekst/kultur er den konkrete situation ”hvem”, ”hvad”, ”hvor”, og ”hvornår”
                    <br>-	Mediet er hvilken måde kommunikationen er bragt ud på som printet medie, elektronisk eller person til person.
                    <br>-	Genre er den måde kontekst/kultur, medie og retoriske strategisk kan arbejde sammen på. 
                </p>
