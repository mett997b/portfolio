            <h2>Det samfundsvidenskabelige paradigme</h2>
            <p>
                Den samfundsvidenskabelige paradigme er det præcist det modsatte af den humanistiske paradigme. 
                <br>Ved den samfundsvidenskabelige er den afsenderen der er i fokus. Den bliver set som den ”gamle”, historiske tilgang da kommunikation bliver forstået som transmission, og at verden er forudsigelig. 
            </p>
            <p>
                Den samfundsvidenskabelige paradigme anvender en meget kvantitativ tilgang, hvor undersøgelsesmetoder med målbare data er en vigtig del.
            </p>
            <p>
                Vigtige karaktertræk for det samfundsvidenskabelige:
                <br>-	Objektivitet
                <br>-	Forklaring
                <br>-	Kvantitativ tilgang
                <br>-	Deduktion
                <br>-	Stabile omgivelser
                <br>-	Afsenderorienteret
                <br>-	Transmission
                <br>-	Handlingsanvisende
                <br>-	Modtager ses som passiv
            </p>
