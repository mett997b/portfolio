            Kommunikationsteorier kan grundlæggende deles op i to paradigmer, der beskriver den måde vi forstår verdens på. 
            <br>De to paradigmer er beskrevet som, det humanistiske paradigme og det samfundsvidenskabelige paradigme.
        </p>

            <h2>Det humanistiske paradigme</h2>
            <p>
                Ved det humanistiske paradigme, er det modtageren der er i fokus og er anses som aktiv og behovstyret. 
                <br>Det humanistiske paradigme er derfor også typisk de humanistiske fag som sprog, kultur, psykologi, kunst o.lign. 
                <br>For at kunne forstå det humanistiske paradigme, er det nødvendigt at man anvender metoder, der giver en dybdegående forståelse for, hvad den enkle modtager oplever. 
                <br> Det er derfor vigtigt at have en kvalitativ tilgang og uanset hvilke intentioner afsender har haft, er det modtageren alene der bestemmer hvordan budskabet bliver fortolket.
            </p>
            <p>
                De vigtigste karaktertræk i det humanistiske paradigme:
                <br>- Kvalitativ tilgang
                <br>- Subjektivitet
                <br>- Fortolkning
                <br>- Induktion
                <br>- Situationsbestemt
                <br>- Modtagerorienteret
                <br>-Interaktion mellem mennesker
                <br>- Mennesket ses som aktiv
            </p>
            <p>
                Det humanistiske paradigme er overordnet set kommunikation mellem mennesker, der skal altid en modtager til at fortolke, hvad end afsender vil ud med.
            </p>
