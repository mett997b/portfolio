                    <h4>S-O-R-modellen</h4>
                    <p>
                        ”O” betyder organismen og er et ekstra del i S-R-modellen. S-O-R-modellen bygger på, at ikke alle opfatter budskaber end, og at der skal tages hensyn. 
                        <br>Det vil sige, at kommunikationen bliver overført gennem et organisme, og dermed ikke direkte.
                    </p>
