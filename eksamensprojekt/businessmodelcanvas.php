            <h2>Kundesegmenter</h2>
            <P>
                Kundesegmenter er den vigtigste byggesten i forretningsmodellen. Uden kunder, ville en virksomhed ikke kunne fungere. Da ingen mennesker er ens, er det derfor svært at gøre alle tilfredse. Som virksomhed vælger man derfor at inddele kunderne i kundesegmenter, de er inddelt efter fællesbehov, adfærdsmønstre og andre karakteristika. 
            </P>
            <p>
                Nogle virksomheder udgangspunkt i massemarked. Det vil sige, at de ikke skelner mellem forskellige kundesegmenter, og dermed fokuser på en større gruppe af kunder, med ca. samme behov og problemer.
            </p>
            <p>
                Andre virksomheder tager udgangspunkt i nichenmarked. Det vil sige, at de henvender sig til specialiseret kundesegmenter, og dermed fokusere på den enkles behov.
            </p>
            <a id="kildelink" href="#">-BMG bog side 26 og 27</a>

            <h2>Værditilbud</h2>
            <p>
                Værditilbud er, ud over kundesegmenter, den vigtigste byggesten i forretningsmodellen. 
                <br>Værditilbud beskriver selve produkterne og/eller ydelserne, en virksomhed tilbyder og sælger. Disse produkter og/eller ydelser, skal selvfølgelig løser problemer elle opfylde et behov hos kunden, for at skabe relevans.
            </p>
            <a id="kildelink" href="#">-BMG bog side 28 og 29</a>

            <h2>Kanaler</h2>
            <p>
                Kanaler er det i forretningsmodellen der beskriver, hvordan virksomheder når ud til kunden på. Det gøres gennem berøringspunkter med kunden, som kommunikation, distribution og salg. 
                <br>Det er vigtigt at være opmærksom på hvilke grænseflader der anvendes, da dette kan have stor betydning for kundeoplevelsen.
            </p>
            <a id="kildelink" href="#">-BMG bog side 32 og 33</a>

            <h2>Kunderelationer</h2>
            <p>
                Kunderelationer er det i forretningsmodellen der beskriver, hvilken relation en virksomhed ønsker at opbygge og/eller vedligeholde med kundesegmenterne. 
                <br>Kunderelationer kan være personlige, automatiseret eller alt derimellem. Udgangspunktet for valg af relation er motiveret af tre motivationsfaktorer:
                <br>-Kundehvervning
                <br>-Kundefastholdelse
                <br>-Mersalg/opsalg
            </p>
            <a id="kildelink" href="#">-BMG bog side 34 og 35</a>

            <h2>Indtægtsstrømme</h2>
            <p>
                Indtægtsstrømmen er det i forretningsmodellen der beskriver, de penge virksomheden produceret gennem kundesegmenterne. 
                <br>Det er vigtigt for en virksomhed at finde ud af, hvor meget hvert kundesegment er villig til at betale, og dermed danne en eller flere indtægtsstrømme.
            </p>
            <p>
                En indtægtsstrøm udformes i to forskellige typer:
                <br>-Transaktionsbaserede indtægter: En gang kundebetaling.
                <br>-Tilbagevendende indtægter: løbende underbetalinger som afdrag eller leasing.
            </p>
            <a id="kildelink" href="#">-BMG bog side 36 og 37</a>

            <h2>Nøgleressourcer</h2>
            <p>
                Nøgleressourcer er den vigtigste del i at få en forretningsmodel til at fungere. For at kunne udbyde og udarbejde forskellige værditilbud, få kontakt til kundesegmenterne og opretholde kommunikationen, er det vigtigt at have de rigtige ressourcer. 
                <br>Nøgleressourcer er altafhængigt af, hvilket produkt eller ydelse, der tilbydes og kan være enten fysisk, immaterielle, menneskelige eller økonomiske.
            </p>
            <a id="kildelink" href="#">-BMG bog side 40 og 41</a>

            <h2>Nøgleaktivitet</h2>
            <p>
                Nøgleaktiviteter er de opgaver, en virksomhed skal løse, for at opretholde deres produkt og/eller ydelse. Nøgleaktiviteter kan opdeles i tre kategorier:
            </p>
            <p>
                - Produktion: Aktiviteter som design, fremstilig og levering.
                <br>- Problemløsning: Opretholde og videreudvikle løsninger, til individuelle kundeproblemer.
                <br>- Platform/netværk: Platformsstyring, serviceprovisionering og platformspromovering.
            </p>
            <a id="kildelink" href="#">-BMG bog side 42 og 43</a>

            <h2>Nøglepartnere</h2>
            <p>
                Nøglepartnere er de samarbejdspartnere en virksomhed har. En virksomhed vælger at indgå i partnerskaber, oftest af økonomiske grunde, til at reducere risiko for at gå fallit. 
                <br>Der findes fire forskellige former for partnerskaber:
            </p>
            <p>
                - Strategiske alliancer mellem ikke-konkurrenter.
                <br>- Coopetetition: Strategiske alliancer mellem konkurrenter.
                <br>- Joint vatures: Til at udvikle nyopstartede forretninger.
                <br>- Køber/leverandør-relationer: Til at skabe sikkerhed omkring pålidelige forsyninger. 
            </p>
            <a id="kildelink" href="#">-BMG bog side 44 og 45</a>

            <h2>Omkostningsstruktur</h2>
            <p>
                Omkostningsstruktur er alle de omkostninger, der har at drive en virksomhed. At skabe og levere værditilbud og ydelser, opretholde kunderelationer og skabe en indtægt, kræver en masse omkostninger. 
                <br>Det er vigtigt for en virksomhed at skabe en balance mellem, at kunden ikke betaler for meget, og at virksomheden danner overskud. 
                <br>Omkostningerne bliver beregnet ud fra, at der er styr på nøgleressourcer, nøgleaktiviteter og nøglepartnere.
            </p>
