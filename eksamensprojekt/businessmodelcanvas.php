        <h1>Businessmodelcanvas</h1>
        <p>
            ”A business model descrivbes the rationale of hos an organization creates, deliver and capturer value.” 
            <br>-	Alex osterwalder
        </p>
        <p>
            Forretningsmodellen er et strategisk ledelsesværktøj, der er kendt i hele verden. Den bruges af virksomheder, til at for overblik og logisk se, hvordan de har tænkt at tjene penge. 
        </p>
        <p>
            Forretningsmodellen er opbygget af ni ”byggesten,” og det er vigtigt, at man forstår alle ni, for størst effektivitet.</p>
