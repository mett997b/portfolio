<section>
        <h1>Designthinking</h1>
            <p>
                Design Thinking er en innovationsmetode, der tager udgangspunkt i selve processen i det at udvikle et produkt. 
                <br>En metode, hvor der søges forståelse fra brugeren, de stereotypiske antagelser bliver udfordret og der bliver omdefineret problemstillinger, der skaber vej til innovative løsninger. 
                <br>Design thinking er delt op i 5 (6) faser – Empathizi, Define, Ideate, Prototype og test (og Implement).
            </p>
            <a id="kildelink" href="https://www.interaction-design.org/literature/topics/design-thinking">-interaction-design.org</a>
            <img src="designthinking.png" alt="Designthinkting model">
        <h2>Empathize – Undersøg brugerens behov</h2>
            <p>
                Første fase i design thinking giver mulighed, for at få en empatisk forståelse for et problemet, der søges en løsning på. 
                <br>Man indsamler en masse viden, om et bestemt emne. Dette gøres typisk ved hjælp af bruger research, som observation, kvantitativ og/eller kvalitativ interviews.
                <br>Den vigtigste del ved empathize fasen i design thinking er, at du som udvikler kan sætte dine egne meninger og antagelser til side, og fokusere brugeren og hvad de reelt set har brug for.
            </p>
            <a id="kildelink" href="https://www.interaction-design.org/literature/topics/design-thinking">-interaction-design.org</a>
        <h2>Define – Definere problemstilling(er)</h2>
            <p>
                I fase to efterbearbejdes de indsamlede informationer fra empathize fasen. Her bliver der analyseret på det og kerneproblemerne defineres. Hvorefter der defineres problemstillinger der skal danne ramme for ideate fasen. 
                <br>Man springer tit frem og tilbage mellem define og empathize faserne, da man oftest opdager nye ting, der kan være nyttige at videre undersøge inden, at man fortsætter til ideate fasen.
            </p>
            <a id="kildelink" href="https://www.interaction-design.org/literature/topics/design-thinking">-interaction-design.org</a>
        <h2>Ideate – Ideudvikling</h2>
            <p>
                Det har indtil nu være ’forbudt’ at tænke i idéer. 
                <br>I idé fasen skal man, med den viden man har fået fra de sidste to faser, komme op med alle de idéer der kan være, til at løse problemstillingerne. 
                <br>Det er også her, at man skal prøve at tænke ud af boksen. Den mest vanvittige idé, kan ende med at blive den mest geniale.
            </p>
            <a id="kildelink" href="https://www.interaction-design.org/literature/topics/design-thinking">-interaction-design.org</a>
        <h2>Prototype – Produktudvikling</h2>
            <p>
                Når idéen er fundet, er det nu tid til at lave en prototype, der har til fordel at give et visuelt billede af de tre foregående faser. 
                <br>Fordelen ved at lave en prototype er, at man kan få aftestet sin idé, inden der bliver investeret en penge i noget, der alligevel ikke bliver brugt, eller købt. 
                <br>Ikke alle idéer kommer længere end idé fasen, da de kan være for komplekse til at blive til virkelighed, og man veksler derfor tit mellem ideate og prototype faserne.
            </p>
            <a id="kildelink" href="https://www.interaction-design.org/literature/topics/design-thinking">-interaction-design.org</a>
        <h2>Test</h2>
            <p>
                Test fasen giver lidt sig selv, her tester man sin prototype. Formålet med at teste prototypen er, at få andre perspektiver på, og ud fra deres syn finde fejl og mangler. Afhængig af hvor store fejlene er, kan det være nødvendigt at gå tilbage og genoverveje, og gå men en anden idé.
            </p>
            <a id="kildelink" href="https://www.interaction-design.org/literature/topics/design-thinking">-interaction-design.org</a>
        <h2>Implement – Send det ud i verden</h2>
            <p>
                Typisk vil design thinking være som 5 faser, men nogle mener at der findes en sjette. 
                <br>Den sjette fase bliver kaldt implement, og er når det færdige produkt bliver sendt ud til forbrugeren.
                <br>Mange mener, at design thinking slutter med denne fase, men i den tid vi lever nu, er et produkt aldrig rigtig færdigt. Hvilket også et grund til at mange ikke medtager implement som en reel fase.

            </p>
